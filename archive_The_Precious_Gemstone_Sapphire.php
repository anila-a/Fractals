	<font size="3">&nbsp;&nbsp;&nbsp;Sapphire is the most precious and valuable blue gemstone. It is a very desirable 
		gemstone due to its excellent color, hardness, durability, and luster. In the gem trade, Sapphire without any color 
		prefix refers to the blue variety of the mineral Corundum. However, the term Sapphire encompasses all other gem 
		varieties and colors of Corundum as well, excluding Ruby, the red variety of Corundum, which has its own name since antiquity.</font>
		<font size="3"><p><img id="archive_images" src="archive_images/sapphire.jpg" width="324" height="240" align="right"/>&nbsp;&nbsp;&nbsp;Sapphire 
		often contains minor inclusions of tiny slender Rutile needles. When present, these inclusions decrease the transparency of a stone 
		and are known as silk. When in dense, parallel groupings, these inclusions can actually enhance by allowing polished Sapphires to 
		exhibit asterism. Sapphire gems displaying asterism are known as "Star Sapphire", and these can be highly prized. Star Sapphire exists 
		in six ray stars, though twelve ray stars are also known.</p></font>
	<font size="3">&nbsp;&nbsp;&nbsp;Sapphire is pleochroic, displaying a lighter and more intense color when 
                viewed at different angles. Some pleochroic Sapphire is blue when viewed at one angle, and purple at a different angle. Color zoning, which forms from growth 
		layers that build up during the formation of the stone, may also be present in certain Sapphires. Color zoning is responsible for 
		certain Sapphires having lighter and darker colors in different parts of a crystal. Some Sapphire gemstones may even be multicolored
		such as purple and blue.</font>
	<p><font size="3">&nbsp;&nbsp;&nbsp;Sapphire is a tough and durable gem, and the only natural gemstone harder than Sapphire is Diamond. Despite 
		this, Sapphire is still subject to chipping and fracture if handled roughly, and care should be taken to ensure it is properly handled. Sapphire was 
		first synthesized in 1902. The process of creating synthetic Sapphire is known as the Verneuil process. Only experts can distinguish between natural 
		and synthetic Sapphire.</font></p>

