	<font size="3">&nbsp;&nbsp;&nbsp;Artificial intelligence has jumped from sci-fi movie plots into mainstream news headlines in just a couple of years.
        And the headlines are often contradictory. AI is either a technological leap into greater prosperity or mass unemployment; it will either be our 
		most valuable servant or terrifying master.</font>
	<font size="3"><p><img id="archive_images" src="archive_images/AI.jpg" width="340" height="217" align="right"/><b>What is Artificial Intelligence</b></p></font>
	<font size="3">&nbsp;&nbsp;&nbsp;AI is a computer system that can do tasks that humans need intelligence to do.<br>
		       &nbsp;&nbsp;&nbsp;A driverless car, for example, relies on multiple sensors to understand where it is and what's around it. These include speed, location, direction and 360-degree 
				   vision. Based on those inputs, among others, the "intelligent" computer system controls the car by deciding, like a human would, when to turn the steering and when 
				   to accelerate or brake.</font>
	<p><font size="3">&nbsp;&nbsp;&nbsp;Then there's machine learning, a subset of AI, which involves teaching computer programs to learn by finding patterns in data. The more data, the more the computer system improves.
				   "Whether it's recognizing objects, identifying people in photos, reading lung scans or transcribing spoken mandarin, if we pick a narrow task like that [and] we give it enough data, the computer learns to do it as well as, 
                   if not better, than us," University of New South Wales professor of artificial intelligence Toby Walsh said. AI doesn't have to sleep or make the same mistake twice. It can also access vast troves of digital data in seconds. 
                   Our brains cannot.</font></p>
    <p><font size="3"><b>Do we use AI?</b><br><br>&nbsp;&nbsp;&nbsp;Yes, probably every day.
				   AI is in your smart phone; it's there every time you ask a question of iPhone's Siri or Amazon's Alexa. It's in your satellite navigation system and instant translation apps.
				   AI algorithms recognise your speech, provide search results, help sort your emails and recommend what you should buy, watch or read.
				   "AI is the new electricity," according to Andrew Ng, former chief scientist at Baidu, one of the leading Chinese web services companies. AI will increasingly be all around you from your phone to your TV, car and home appliances.</font></p>
