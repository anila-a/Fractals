	<font size="5" face="Berlin Sans FB">Astrobiology</font>
	<div class="hline"></div>
	<font size="3">&nbsp;&nbsp;&nbsp;Astrobiology is the study of the origin, evolution, distribution, and future of life in the universe: 
                extraterrestrial life and life on Earth. Astrobiology addresses the question of whether life exists beyond Earth, and how humans can 
                detect it if it does. The term exobiology is similar but more specific—it covers the search for life beyond Earth, and the effects 
                of extraterrestrial environments on living things.</font>
	<font size="3"><p><img id="archive_images" src="archive_images/astrobiology.png" width="370" height="217" align="right"/>&nbsp;&nbsp;&nbsp;Astrobiology 
		makes use of physics, chemistry, astronomy, biology, molecular biology, ecology, planetary science, geography, and geology to investigate the 
		possibility of life on other worlds and help recognize biospheres that might be different from that on Earth. The origin and early evolution 
		of life is an inseparable part of the discipline of astrobiology. Astrobiology concerns itself with interpretation of existing scientific data, 
		and although speculation is entertained to give context, astrobiology concerns itself primarily with hypotheses that fit firmly into existing 
		scientific theories. Given more detailed and reliable data from other parts of the universe, the roots of astrobiology itself—physics, chemistry 
		and biology—may have their theoretical bases challenged.</p></font>
	<font size="3">&nbsp;&nbsp;&nbsp;This interdisciplinary field encompasses research on the origin and evolution of planetary systems, origins of organic 
                compounds in space, rock-water-carbon interactions, abiogenesis on Earth, planetary habitability, research on biosignatures for life detection, and studies 
                on the potential for life to adapt to challenges on Earth and in outer space.</font>
	<p><font size="3">&nbsp;&nbsp;&nbsp;The chemistry of life may have begun shortly after the Big Bang, 13.8 billion years ago, during a habitable epoch when 
                the Universe was only 10–17 million years old. According to the panspermia hypothesis, microscopic life—distributed by meteoroids, asteroids and 
                other small Solar System bodies—may exist throughout the universe. According to research published in August 2015, very large galaxies may be more favorable 
                to the creation and development of habitable planets than such smaller galaxies as the Milky Way. Nonetheless, Earth is the only place in the universe humans 
                know to harbor life. Estimates of habitable zones around other stars, sometimes referred to as "Goldilocks zones," along with the discovery of hundreds 
                of extrasolar planets and new insights into extreme habitats here on Earth, suggest that there may be many more habitable places in the universe than considered possible 
                until very recently.</font></p>
	<p><font size="3">&nbsp;&nbsp;&nbsp;Current studies on the planet Mars by the Curiosity and Opportunity rovers are searching for evidence of ancient life as well as plains 
                related to ancient rivers or lakes that may have been habitable. The search for evidence of habitability, taphonomy (related to fossils), and organic molecules on the planet 
                Mars is now a primary NASA and ESA objective.</font></p>

